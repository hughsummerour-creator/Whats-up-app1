export const formatNumber = (num: number): string => {
  if (num >= 1000000) {
    return (num / 1000000).toFixed(1).replace(/\.0$/, '') + 'M';
  }
  if (num >= 1000) {
    return (num / 1000).toFixed(1).replace(/\.0$/, '') + 'K';
  }
  return num.toString();
};

// Format hours for display
export const formatHours = (hours?: BusinessHours): string => {
  if (!hours) return 'Open 24/7';
  
  const today = new Date().getDay();
  const dayNames = ['sunday', 'monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday'];
  const todayHours = hours[dayNames[today] as keyof BusinessHours];
  
  if (!todayHours || todayHours.isClosed) return 'Closed today';
  return `Open today ${todayHours.open} - ${todayHours.close}`;
};
